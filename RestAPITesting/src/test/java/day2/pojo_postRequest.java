package day2;

public class pojo_postRequest {
	String st_name;
	int st_fee;
	String[] st_courses;
	int st_phoneNo;
	
	public String getSt_name() {
		return st_name;
	}
	public void setSt_name(String st_name) {
		this.st_name = st_name;
	}
	public int getSt_fee() {
		return st_fee;
	}
	public void setSt_fee(int st_fee) {
		this.st_fee = st_fee;
	}
	public String[] getSt_courses() {
		return st_courses;
	}
	public void setSt_courses(String[] st_courses) {
		this.st_courses = st_courses;
	}
	public int getSt_phoneNo() {
		return st_phoneNo;
	}
	public void setSt_phoneNo(int st_phoneNo) {
		this.st_phoneNo = st_phoneNo;
	}

}
